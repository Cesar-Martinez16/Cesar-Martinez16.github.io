class ForSum {
    public static void main (String[] args) {

        int sum = 0;
        int n = 4;

        // for loop
        for (int i = 1; i <= n; ++i) {
            // body inside for loop
            sum += i;      // stum = stum + i
            System.out.println(sum);
        }

        System.out.println("Sum = " + sum);
        System.out.println("Avg = " );
    }
    
}
